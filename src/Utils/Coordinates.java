package Utils;

public class Coordinates {

    private int X, Y;

    public Coordinates(int x, int y){
        this.X = x;
        this.Y = y;
    }

    public int getX(){ return X; }
    public int getY(){ return Y; }

    public void setX(int x){ X = x; }
    public void setY(int y){ Y = y; }

}
